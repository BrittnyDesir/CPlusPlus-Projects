#include <iostream>
using namespace std;

int main() {

    cout << "The size of an unsigned int in bytes is: " << sizeof(unsigned int) << endl;
    cout << "The size of an int in bytes is: " << sizeof(int) << endl;
    cout << "The size of a long int in bytes is: " << sizeof(long int) << endl;

    return 0;
}
